#include "3-calc.h"
/**
 * op_add- perform addition
 * @a: first number
 * @b: second number
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - perform subtraction
 * @a: first num
 * @b: second num
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - perform multiplication
 * @a: first number
 * @b: second number
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - perform division
 * @a: first number
 * @b: second number
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - perform modulo operation
 * @a: first number
 * @b: second number
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
